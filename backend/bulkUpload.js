const runBulkUpload = async () => {
    try {
        await connectDB();
        console.log('MongoDB connected');

        for (const p of productsData) {
            console.log(`Processing product: ${p.name}`);
            
            // Upload images to Cloudinary
            const cloudinaryUrls = await Promise.all(p.image.map(async (imgFileName) => {
                const imgPath = path.resolve(__dirname, '../frontend/src/assets', imgFileName);
                try {
                    const result = await cloudinary.uploader.upload(imgPath, {
                        resource_type: 'image'
                    });
                    return result.secure_url;
                } catch (uploadError) {
                    console.error(`Failed to upload ${imgFileName}:`, uploadError.message);
                    throw uploadError;
                }
            }));

            // Remove _id to let MongoDB generate a valid ObjectId
            delete p._id;

            // Save to DB
            const newProduct = new productModel({
                ...p,
                image: cloudinaryUrls
            });

            await newProduct.save();
            console.log(`Successfully uploaded: ${p.name}`);
        }

        console.log('Bulk upload completed successfully!');
        process.exit(0);
    } catch (error) {
        console.error('Bulk upload failed:', error);
        process.exit(1);
    }
};

runBulkUpload();
